	/************************************************************************/
	/* 锁定鼠标                                                     					              */
	/************************************************************************/
	/** 锁定鼠标在矩形区域
	 * 一 设置区域
	 * 二 锁定
	 * 三 在适当的时候释放鼠标
	*/

	//设置区域
	RECT rc;
	rc.left = 50;
	rc.top = 50;
	rc.right = 50;
	rc.bottom = 50;

	//锁定鼠标
	::ClipCursor(&rc);

	::Sleep(3000); //锁定3秒

	//解除鼠标锁定
	::ClipCursor(NULL);
